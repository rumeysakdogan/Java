package day29_CustomMethods;
/*
    step1: print hello 5 times
    step2: print your school name
    step3: print hello 5 times
    step4: print your name
    step5: print hello 5 times
 */
public class MethodWithOutParameter {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("Hello!");
        }

        System.out.println("My school is Cybertek School");

        for (int i = 0; i < 5; i++) {
            System.out.println("Hello!");
        }

        System.out.println("My name is Rumeysa Dogan");

        for (int i = 0; i < 5; i++) {
            System.out.println("Hello!");
        }
    }
}
