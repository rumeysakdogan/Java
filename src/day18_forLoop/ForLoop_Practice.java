package day18_forLoop;

public class ForLoop_Practice {
    public static void main(String[] args) {

        // iterator examples: ++i, i+=1 , i+=5, i+=2

        for(int i = 1; i <= 5; i++) {
            System.out.println("Cybertek Batch 20");
        }

        System.out.println("==========================");

        for(int i = 1; i < 11; i++) {
            System.out.print(i+" ");
        }

        System.out.println("\nHello");

        System.out.println("==========================");

        for(int i = 30; i >= 1; i--) {
            System.out.print(i+" ");
        }

        System.out.println("\n==========================");

        for(int i = 0; i <= 100; i+=2) {
            System.out.print(i+" ");
        }

        System.out.println("\n==========================");

        for(int i = 1; i <= 100; i+=2) {
            System.out.print(i+" ");
        }

        System.out.println("\n==========================");

        for(int i = 65; i < 91; i++) {
            System.out.print((char)i+" ");
        }

        System.out.println("\n==========================");

        for(int i = 97; i <= 122; i++) {
            System.out.print((char)i+" ");
        }

        System.out.println("\n==========================");

        for(char i = 'a'; i <= 'z'; i++) {
            System.out.print(i+" ");
        }

        System.out.println("\n==========================");

        for(char i = 'A'; i <= 'Z'; i++) {
            System.out.print(i+" ");
        }


    }
}
