package day18_forLoop;

public class ForLoop {
    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        System.out.println("=======================");

        for (int i = 0; i < 5; i++){    // i: 0, 1, 2, 3, 4
            System.out.println("Hello World");
        }

    }
}
