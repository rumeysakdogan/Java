package day22_NestedLoop;

public class NestedLoop_3 {
    public static void main(String[] args) {

        int i = 0;
        while (i < 4){

            for (int j = 0; j < 10; j++){
                System.out.print("*");
            }
            System.out.println();
            i++;

        }



    }
}
