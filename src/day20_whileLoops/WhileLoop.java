package day20_whileLoops;

public class WhileLoop {
    public static void main(String[] args) {

        if(true){   // if statement gets executed once
            System.out.println("Hello World");
        }

        System.out.println("========================");

        while(true){    // repeated if statement
            System.out.println("Hello World");
        }
        /*
            for ( int i = 0; i == 0; )
        */
    }
}
